    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField1.getText(); // Register username variable
        String password = jPasswordField2.getText(); // Register password variable
        
        if (username.equals("") || password.equals("")) { // If password is equal to ""
            jLabel4.setText("You're missing a field!"); // SEt text
            return;
        }
        
        if (StartFrame.findByName(username) == null) { // If profile is null
            jLabel4.setText("That username doesn't exist!"); // Set text
            return;
        }
        
        Profile profile = StartFrame.findByName(username); // Get profle
        
        if (!profile.password.equals(password)) { // Check if password is incorrect
            jLabel1.setForeground(new java.awt.Color(255, 51, 51)); // Set password background to red
            jLabel4.setText("Invalid password! Please try again!"); // Set text
            return;
        }
        
        setVisible(false); // CLose this frame
        new ProfileFrame(profile).setVisible(true); // Open new frame
    }//GEN-LAST:event_jButton1ActionPerformed

